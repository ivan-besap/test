package com.eVolGreen.eVolGreen.Models.Ocpp.Ocpp2_0.Models.Core.Requests.Enums;

/**
 * Enumeración de versiones de OCPP.
 *
 * <p>Representa las diferentes versiones del protocolo OCPP (Open Charge Point Protocol) que
 * pueden ser utilizadas en las funciones de comunicación de estaciones de carga.
 */
public enum OCPPVersionEnum {
    /** Versión 1.2 del protocolo OCPP. */
    OCPP12,

    /** Versión 1.5 del protocolo OCPP. */
    OCPP15,

    /** Versión 1.6 del protocolo OCPP, ampliamente adoptada. */
    OCPP16,

    /** Versión 2.0 del protocolo OCPP, incluye soporte para características avanzadas. */
    OCPP20
}
