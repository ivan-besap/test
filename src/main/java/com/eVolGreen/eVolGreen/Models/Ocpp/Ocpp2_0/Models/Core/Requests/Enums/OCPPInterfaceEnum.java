/**
 * Enumeración de interfaces de red aplicables.
 *
 * <p>Define las interfaces de red disponibles para la comunicación en el protocolo OCPP. Estas
 * incluyen tanto interfaces cableadas como inalámbricas.
 */
public enum OCPPInterfaceEnum {
    /** Interfaz cableada 0. */
    Wired0,

    /** Interfaz cableada 1. */
    Wired1,

    /** Interfaz cableada 2. */
    Wired2,

    /** Interfaz cableada 3. */
    Wired3,

    /** Interfaz inalámbrica 0. */
    Wireless0,

    /** Interfaz inalámbrica 1. */
    Wireless1,

    /** Interfaz inalámbrica 2. */
    Wireless2,

    /** Interfaz inalámbrica 3. */
    Wireless3
}
