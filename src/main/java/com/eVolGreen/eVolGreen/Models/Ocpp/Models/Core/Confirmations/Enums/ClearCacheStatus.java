/**
 * Enum que define los posibles resultados de una solicitud de limpieza de caché en OCPP.
 */
public enum ClearCacheStatus {
    /**
     * Indica que la solicitud de limpiar la caché fue aceptada.
     */
    Accepted,

    /**
     * Indica que la solicitud de limpiar la caché fue rechazada.
     */
    Rejected
}
